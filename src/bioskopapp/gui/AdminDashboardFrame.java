    private void addPanelsToTabbedPane() {
        filmPanel = new FilmManagementPanel();
        schedulePanel = new ScheduleManagementPanel();
        salesPanel = new SalesReportPanel();

        jTabbedPaneAdmin.addTab("Kelola Film", filmPanel);
        jTabbedPaneAdmin.addTab("Kelola Jadwal", schedulePanel);
        jTabbedPaneAdmin.addTab("Laporan Penjualan", salesPanel);
    }
    
    private void addTabListener() {
        jTabbedPaneAdmin.addChangeListener(new ChangeListener() {
            @Override
            public void stateChanged(ChangeEvent e) {
                int selectedIndex = jTabbedPaneAdmin.getSelectedIndex();
                if (selectedIndex == 1) { 
                    schedulePanel.loadComboBoxData();
                    schedulePanel.loadTableData();
                } else if (selectedIndex == 2) {
                    salesPanel.loadReportData();
                }
            }
        });
    }

